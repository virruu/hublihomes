function faqFor(p) {
  const items = [
    {
      question: `Is this ${p.bhk} BHK property suitable for bachelors?`,
      answer:
        p.bachelors === "Allowed"
          ? "Yes, bachelors are allowed at this property."
          : "No, this listing is for families only. Bachelors are not allowed.",
    },
    {
      question: "Is parking available?",
      answer: `Yes, ${p.parking.toLowerCase()} is available.`,
    },
    {
      question: "How far is the nearest bus stop?",
      answer: `The nearest bus stop is ${p.busStop.toLowerCase()}.`,
    },
  ];
  if (p.vastu) {
    items.push({
      question: "Is the property Vastu compliant?",
      answer: `Yes, the ${p.propertyType.toLowerCase()} is ${p.facing.toLowerCase()}-facing and Vastu compliant.`,
    });
  }
  return items;
}

function toMdx(p) {
  const yaml = [
    `slug: ${p.slug}`,
    `title: ${p.title}`,
    `listing: Rent`,
    `status: Available`,
    `propertyType: ${p.propertyType}`,
    `bhk: ${p.bhk}`,
    `price: ${p.price}`,
    `priceSuffix: /month`,
    `area: ${p.area}`,
    `locality: ${p.locality}`,
    `city: Hubli`,
    `facing: ${p.facing}`,
    `vastu: ${p.vastu}`,
    `vegetarian: ${p.vegetarian}`,
    `bachelors: ${p.bachelors}`,
    `family: ${p.family}`,
    `parking: ${p.parking}`,
    `bathrooms: ${p.bathrooms}`,
    `furnished: ${p.furnished}`,
    `featured: ${p.featured}`,
    `isNew: true`,
    `amenities:`,
    ...p.amenities.map((a) => `  - ${a}`),
    `rules:`,
    ...p.rules.map((r) => `  - ${r}`),
    `nearby:`,
    `  schools:`,
    ...p.schools.map((s) => `    - ${s}`),
    `  hospitals:`,
    ...p.hospitals.map((h) => `    - ${h}`),
    `  busStop: ${p.busStop}`,
    `  railway: ${p.railway}`,
    `faq:`,
    ...faqFor(p).flatMap((f) => [
      `  - question: ${f.question}`,
      `    answer: ${f.answer}`,
    ]),
    `mapQuery: ${p.mapQuery}`,
    `coverImage: /images/properties/${p.slug}/cover.webp`,
    `gallery:`,
    `  - /images/properties/${p.slug}/1.webp`,
    `  - /images/properties/${p.slug}/2.webp`,
  ].join("\n");

  return `---\n${yaml}\n---\n${p.description}\n`;
}

